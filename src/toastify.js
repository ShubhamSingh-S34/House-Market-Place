import { ToastContainer, toast } from 'react-toastify';

export const toastifySuccess=(message)=>{
    toast.success(message, {
        position: "top-right",
        autoClose: 3000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "light",
        // transition:'slide',
        });
}

